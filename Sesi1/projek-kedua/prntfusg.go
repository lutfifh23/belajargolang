package main

import "fmt"

func main() {
	first, second, third := "Aurell", 21, "Jakarta"

	fmt.Printf("Ini adalah menampilkan %T \n", first)  //menampilkan tipe data
	fmt.Printf("Ini adalah menampilkan %d \n", second) //menampilkan integer
	fmt.Printf("Ini adalah menampilkan %s \n", third)  //menampilkan string
}
